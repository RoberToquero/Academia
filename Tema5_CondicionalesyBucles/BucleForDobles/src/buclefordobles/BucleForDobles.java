public class BucleForDobles {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int x,y;
        //La variable que controla el bucle es x hasta que sea 10
        for(x=1,y=2;x<=10;x++,y++){
            System.out.println("Elemento: "+x+"\t"+"Elemento: " +y);
            
        }
    }
    
}
