public class FinalCapitulo3 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       
        int a=-5,b=3,c=12;
        int op1=a*b*c;
        int op2=a*b-c*a;
        int op3=-c+b*(a-b);
        double op4=(a*b-c*a)/Math.pow(a,2);
        double op5=b*(a-b)*c%(c+b);
        double op6=Math.pow(c,0.3333);
        double op7=Math.sqrt(Math.pow(c,2)+ b*Math.pow(a,3));
        double op8=Math.log(b*(-a+b));
        double op9=Math.exp(a);
        System.out.println(op1);
        System.out.println(op2);
        System.out.println(op3);
        System.out.println(op4);
        System.out.println(op5);
        System.out.println(op6);
        System.out.println(op7);
        System.out.println(op8);
        System.out.println(op9);
        
    }
    
}
